def one_hot_encode_dna(sequence):
    """
    Converts a DNA sequence string into a One-Hot Encoded NumPy matrix.

    Args:
        sequence (str): Raw DNA string (e.g., "ATGCNT").

    Returns:
        np.ndarray: A matrix of shape (Length, 4) suitable for 1D CNNs.
    """
    # Standardize the sequence to uppercase
    sequence = sequence.upper()

    # Define the encoding dictionary
    # Using arrays for fast mapping. 'N' represents an unknown base.
    mapping = {
        'A': [1, 0, 0, 0],
        'C': [0, 1, 0, 0],
        'G': [0, 0, 1, 0],
        'T': [0, 0, 0, 1],
        'N': [0, 0, 0, 0]  # Alternatively, use [0.25, 0.25, 0.25, 0.25]
    }

    # Initialize a matrix of zeros: Shape (Sequence Length, 4 Channels)
    encoded_matrix = np.zeros((len(sequence), 4), dtype=np.float32)

    # Populate the matrix
    for i, base in enumerate(sequence):
        # dict.get() provides a fallback for unexpected characters (treats them as 'N')
        encoded_matrix[i] = mapping.get(base, [0, 0, 0, 0])

    return encoded_matrix

