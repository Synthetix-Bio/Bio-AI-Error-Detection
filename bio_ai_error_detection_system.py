"""### 7. Base Composition Plot

A base composition plot shows the proportion of Adenine (A), Cytosine (C), Guanine (G), and Thymine (T) in your sequenced data. Significant deviations from expected values can indicate contamination or sequencing bias.
"""

from collections import Counter

def plot_base_composition(fastq_file, num_reads_to_sample=10000):
    base_counts = Counter()
    total_bases = 0
    print(f"Analyzing base composition from {fastq_file}...")

    for i, record in enumerate(SeqIO.parse(fastq_file, "fastq")):
        base_counts.update(record.seq.upper())
        total_bases += len(record.seq)
        if i >= num_reads_to_sample -1:
            break

    # Filter for standard DNA bases
    dna_bases = {base: base_counts[base] for base in ['A', 'C', 'G', 'T'] if base in base_counts}

    if total_bases == 0:
        print("No bases found to plot.")
        return

    percentages = {base: (count / total_bases) * 100 for base, count in dna_bases.items()}

    labels = percentages.keys()
    sizes = percentages.values()
    colors = ['#FF6666', '#66B2FF', '#FFD966', '#8BEE8B'] # A, C, G, T

    plt.figure(figsize=(7, 7))
    plt.pie(sizes, labels=labels, autopct='%1.1f%%', startangle=90, colors=colors)
    plt.title('Overall Base Composition')
    plt.axis('equal')  # Equal aspect ratio ensures that pie is drawn as a circle.
    plt.show()

# Example usage with your FASTQ file
