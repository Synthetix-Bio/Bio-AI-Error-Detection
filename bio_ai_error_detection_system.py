"""### 10. Average Quality Score per Base Position

This plot illustrates the average quality score at each position along the reads. It can help identify if quality degrades towards the ends of reads or if there are specific problematic regions.
"""

def plot_avg_quality_per_position(fastq_file, read_length_limit=1000, num_reads_to_sample=10000):
    quality_sums = np.zeros(read_length_limit)
    position_counts = np.zeros(read_length_limit)

    print(f"Calculating average quality per position from {fastq_file}...")

    for i, record in enumerate(SeqIO.parse(fastq_file, "fastq")):
        scores = record.letter_annotations["phred_quality"]
        for pos, score in enumerate(scores):
            if pos < read_length_limit:
                quality_sums[pos] += score
                position_counts[pos] += 1
        if i >= num_reads_to_sample - 1:
            break

    # Avoid division by zero
    avg_qualities = np.divide(quality_sums, position_counts, out=np.zeros_like(quality_sums, dtype=float), where=position_counts!=0)

    plt.figure(figsize=(12, 6))
    plt.plot(avg_qualities, color='darkgreen', linewidth=2)
    plt.title('Average Quality Score per Base Position')
    plt.xlabel('Position in Read (bp)')
    plt.ylabel('Average Quality Score')
    plt.ylim(0, 40) # Phred scores typically range from 0 to 40
    plt.grid(True, linestyle='--', alpha=0.7)
    plt.show()

# Example usage with your FASTQ file
plot_avg_quality_per_position('/content/SRR13453303.fastq')

