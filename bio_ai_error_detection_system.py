def train_polisher(model, train_dataloader, num_epochs=10, learning_rate=1e-4):
    # Move model to GPU if available
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    model.to(device)
    print(f"Training on {device}...")

    # 1. Define Loss and Optimizer
    # CrossEntropyLoss automatically applies Softmax, so do NOT put Softmax in the model's forward pass
    criterion = nn.CrossEntropyLoss()

    # AdamW is generally preferred over standard Adam for Transformer architectures
    optimizer = optim.AdamW(model.parameters(), lr=learning_rate, weight_decay=1e-4)

    # 2. Training Loop
    for epoch in range(num_epochs):
        model.train()  # Set model to training mode
        epoch_loss = 0.0

        # Iterate over batches of data
        # inputs: (Batch, 4, 100) -> One-hot noisy reads
        # targets: (Batch, 100)   -> Integer class labels (0=A, 1=C, 2=G, 3=T, 4=Gap)
        for batch_idx, (inputs, targets) in enumerate(train_dataloader):

            # Move data to the correct device
            inputs, targets = inputs.to(device), targets.to(device)

            # Zero the gradients to prevent accumulation from previous steps
            optimizer.zero_grad()

            # Forward pass: Get predictions
            # logits shape: (Batch, 100, 5)
            logits = model(inputs)

            # --- CRITICAL PYTORCH FORMATTING STEP ---
            # PyTorch's CrossEntropyLoss expects:
            # Inputs: (Batch, Num_Classes, Sequence_Length)
            # Targets: (Batch, Sequence_Length) containing class indices
            logits = logits.permute(0, 2, 1) # Reshape to (Batch, 5, 100)

            # Calculate Loss
            loss = criterion(logits, targets)

            # Backward pass: Compute gradients
            loss.backward()

            # Gradient Clipping (Crucial for Transformers to prevent exploding gradients)
            torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)

            # Update weights
            optimizer.step()

            epoch_loss += loss.item()

        # Print average loss for the epoch
        avg_loss = epoch_loss / len(train_dataloader)
        print(f"Epoch [{epoch+1}/{num_epochs}] | Average Loss: {avg_loss:.4f}")

    print("Training Complete.")
    return model

