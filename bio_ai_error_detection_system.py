import torch
import torch.nn as nn
import torch.nn.functional as F

class GenomePolisher(nn.Module):
    def __init__(self, seq_len=100, in_channels=4, d_model=64, num_heads=8, num_layers=2, num_classes=5):
        """
        Hybrid CNN-Transformer model for DNA error correction.

        Args:
            seq_len: Length of the input DNA sequence (100bp).
            in_channels: 4 for One-Hot Encoded DNA (A, C, G, T).
            d_model: The number of features extracted per base by the CNN.
            num_heads: Number of attention heads in the Transformer.
            num_layers: Number of Transformer encoder layers.
            num_classes: Output classes per base. E.g., 5 (A, C, G, T, and 'Gap' for deletions).
        """
        super(GenomePolisher, self).__init__()

        # 1. Local Feature Extraction: 1D Convolution
        # A kernel size of 5 means we are looking at 5-mer contexts at a time.
        # padding=2 ensures our output sequence length remains exactly 100.
        self.conv1d = nn.Conv1d(
            in_channels=in_channels,
            out_channels=d_model,
            kernel_size=5,
            padding=2
        )
        self.batch_norm = nn.BatchNorm1d(d_model)

        # 2. Positional Encoding
        # Transformers have no inherent sense of sequence order. Since DNA directionality
        # (5' to 3') is critical, we add learnable positional embeddings.
        self.pos_embedding = nn.Parameter(torch.randn(1, seq_len, d_model))

        # 3. Global Context: Transformer Encoder
        encoder_layer = nn.TransformerEncoderLayer(
            d_model=d_model,
            nhead=num_heads,
            dim_feedforward=d_model * 4,
            dropout=0.1,
            batch_first=True # Ensures input shape is (Batch, Seq_Len, Features)
        )
        self.transformer = nn.TransformerEncoder(encoder_layer, num_layers=num_layers)

        # 4. Polishing Output
        # Projects the learned features back to a probability distribution for each base
        self.fc_out = nn.Linear(d_model, num_classes)

    def forward(self, x):
        """
        Forward pass of the Genome Polisher.

        Expected Input shape 'x': (Batch_Size, Channels=4, Seq_Len=100)
        """
        # Step 1: Extract local motifs
        x = self.conv1d(x)                 # Shape: (Batch, 64, 100)
        x = self.batch_norm(x)             # Normalize features
        x = F.relu(x)                      # Apply non-linearity

        # Step 2: Prepare for the Transformer
        # PyTorch Conv1d outputs (Batch, Channels, Length)
        # Transformers expect (Batch, Length, Features/Channels)
        x = x.permute(0, 2, 1)             # Shape transforms to: (Batch, 100, 64)

        # Inject positional awareness so the attention heads know *where* bases are
        x = x + self.pos_embedding

        # Step 3: Multi-Head Attention
        # The network cross-references bases against each other to identify errors
        x = self.transformer(x)            # Shape remains: (Batch, 100, 64)

        # Step 4: Base-by-base prediction
        logits = self.fc_out(x)            # Shape: (Batch, 100, 5)

        return logits

